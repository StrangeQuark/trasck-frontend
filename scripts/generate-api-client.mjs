import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';

const root = process.cwd();
const sourcePath = process.env.TRASCK_OPENAPI_SOURCE;
const openApiUrl = process.env.TRASCK_OPENAPI_URL || 'http://localhost:6100/v3/api-docs';
const schemaOut = resolve(root, process.env.TRASCK_OPENAPI_OUT || 'src/api/generated/openapi.json');
const clientOut = resolve(root, process.env.TRASCK_API_CLIENT_OUT || 'src/api/generated/trasckApi.ts');

const HTTP_METHODS = ['get', 'post', 'put', 'patch', 'delete'];

async function main() {
  const openapi = sourcePath ? await readJson(resolve(root, sourcePath)) : await fetchOpenApi(openApiUrl);
  await writeJson(schemaOut, openapi);
  await writeFileEnsured(clientOut, generateClient(openapi));
  console.log(`Generated ${relative(clientOut)} from ${sourcePath || openApiUrl}`);
}

async function readJson(path) {
  return JSON.parse(await readFile(path, 'utf8'));
}

async function fetchOpenApi(url) {
  const response = await fetch(url);
  if (!response.ok) {
    throw new Error(`Failed to fetch OpenAPI from ${url}: HTTP ${response.status}`);
  }
  return response.json();
}

async function writeJson(path, value) {
  await writeFileEnsured(path, `${JSON.stringify(value, null, 2)}\n`);
}

async function writeFileEnsured(path, contents) {
  await mkdir(dirname(path), { recursive: true });
  await writeFile(path, contents);
}

function generateClient(openapi) {
  const schemas = openapi.components?.schemas || {};
  const schemaTypes = Object.entries(schemas)
    .map(([name, schema]) => componentType(name, schema))
    .join('\n\n');
  const { apiPaths, operationMap } = pathTypes(openapi.paths || {});
  return `/* eslint-disable */
// Generated by scripts/generate-api-client.mjs from Trasck OpenAPI.
// Do not edit this file manually.

export type UUID = string;
export type ISODate = string;
export type ISODateTime = string;
export type JsonValue = null | boolean | number | string | JsonValue[] | { [key: string]: JsonValue };

${schemaTypes}

export type HttpMethod = 'get' | 'post' | 'put' | 'patch' | 'delete';

export interface CursorPage<T> {
  items: T[];
  nextCursor?: string | null;
  hasMore: boolean;
  limit: number;
}

export interface ApiPaths {
${indent(apiPaths, 2)}
}

export const apiOperations = {
${indent(operationMap, 2)}
} as const;

export type ApiOperationId = keyof typeof apiOperations;
export type ApiPath = keyof ApiPaths;
export type ApiPathMethod<P extends ApiPath> = keyof ApiPaths[P] & HttpMethod;
export type ApiPathOperation<P extends ApiPath, M extends ApiPathMethod<P>> = ApiPaths[P][M];

export type ApiRequestOptions<T> = {
  path?: T extends { path: infer P } ? P : never;
  query?: T extends { query: infer Q } ? Q : never;
  body?: T extends { body: infer B } ? B : never;
  headers?: Record<string, string>;
  signal?: AbortSignal;
  responseType?: 'json' | 'blob' | 'text' | 'void';
};

export interface TrasckApiClientOptions {
  baseUrl?: string;
  credentials?: RequestCredentials;
  defaultHeaders?: Record<string, string>;
  getAccessToken?: () => string | undefined | Promise<string | undefined>;
  getCsrfToken?: () => string | undefined | Promise<string | undefined>;
}

export class ApiError extends Error {
  readonly status: number;
  readonly body: unknown;
  readonly headers: Headers;

  constructor(status: number, body: unknown, headers: Headers) {
    super(\`Trasck API request failed with HTTP \${status}\`);
    this.name = 'ApiError';
    this.status = status;
    this.body = body;
    this.headers = headers;
  }
}

export class TrasckApiClient {
  private readonly baseUrl: string;
  private readonly credentials: RequestCredentials;
  private readonly defaultHeaders: Record<string, string>;
  private readonly getAccessToken?: TrasckApiClientOptions['getAccessToken'];
  private readonly getCsrfToken?: TrasckApiClientOptions['getCsrfToken'];

  constructor(options: TrasckApiClientOptions = {}) {
    this.baseUrl = options.baseUrl ?? '';
    this.credentials = options.credentials ?? 'include';
    this.defaultHeaders = options.defaultHeaders ?? {};
    this.getAccessToken = options.getAccessToken;
    this.getCsrfToken = options.getCsrfToken;
  }

  async request<P extends ApiPath, M extends ApiPathMethod<P>>(
    method: M,
    pathTemplate: P,
    options: ApiRequestOptions<ApiPathOperation<P, M>> = {}
  ): Promise<ApiPathOperation<P, M>['response']> {
    const headers = new Headers(this.defaultHeaders);
    for (const [key, value] of Object.entries(options.headers ?? {})) {
      headers.set(key, value);
    }

    const token = await this.getAccessToken?.();
    if (token && !headers.has('Authorization')) {
      headers.set('Authorization', \`Bearer \${token}\`);
    }

    const upperMethod = method.toUpperCase();
    if (isUnsafeMethod(upperMethod)) {
      const csrfToken = await this.getCsrfToken?.();
      if (csrfToken && !headers.has('X-CSRF-TOKEN')) {
        headers.set('X-CSRF-TOKEN', csrfToken);
      }
    }

    const init: RequestInit = {
      method: upperMethod,
      headers,
      credentials: this.credentials,
      signal: options.signal
    };

    if (options.body !== undefined && options.body !== null) {
      if (isBodyInit(options.body)) {
        init.body = options.body;
      } else {
        headers.set('Content-Type', headers.get('Content-Type') ?? 'application/json');
        init.body = JSON.stringify(options.body);
      }
    }

    const response = await fetch(this.url(pathTemplate as string, options.path, options.query), init);
    if (!response.ok) {
      throw new ApiError(response.status, await readErrorBody(response), response.headers);
    }
    return readResponse(response, options.responseType) as Promise<ApiPathOperation<P, M>['response']>;
  }

  async operation<O extends ApiOperationId>(
    operationId: O,
    options: ApiRequestOptions<ApiPathOperation<(typeof apiOperations)[O]['path'], (typeof apiOperations)[O]['method']>> = {}
  ) {
    const operation = apiOperations[operationId];
    return this.request(operation.method, operation.path, options);
  }

  private url(pathTemplate: string, pathParams?: unknown, queryParams?: unknown) {
    const path = pathTemplate.replace(/\\{([^}]+)\\}/g, (_, key) => {
      const value = getRecordValue(pathParams, key);
      if (value === undefined || value === null) {
        throw new Error(\`Missing path parameter: \${key}\`);
      }
      return encodeURIComponent(String(value));
    });
    const url = new URL(path, this.baseUrl || window.location.origin);
    appendQuery(url, queryParams);
    return this.baseUrl ? url.toString() : url.pathname + url.search;
  }
}

export async function collectCursorPages<T>(loadPage: (cursor?: string) => Promise<CursorPage<T>>) {
  const items: T[] = [];
  let cursor: string | undefined;
  for (;;) {
    const page = await loadPage(cursor);
    items.push(...page.items);
    if (!page.hasMore || !page.nextCursor) {
      return items;
    }
    cursor = page.nextCursor;
  }
}

function appendQuery(url: URL, queryParams: unknown) {
  if (!queryParams || typeof queryParams !== 'object') {
    return;
  }
  for (const [key, rawValue] of Object.entries(queryParams as Record<string, unknown>)) {
    if (rawValue === undefined || rawValue === null || rawValue === '') {
      continue;
    }
    const values = Array.isArray(rawValue) ? rawValue : [rawValue];
    for (const value of values) {
      if (value !== undefined && value !== null) {
        url.searchParams.append(key, String(value));
      }
    }
  }
}

function getRecordValue(value: unknown, key: string) {
  return value && typeof value === 'object' ? (value as Record<string, unknown>)[key] : undefined;
}

function isUnsafeMethod(method: string) {
  return method === 'POST' || method === 'PUT' || method === 'PATCH' || method === 'DELETE';
}

function isBodyInit(value: unknown): value is BodyInit {
  return typeof FormData !== 'undefined' && value instanceof FormData
    || typeof Blob !== 'undefined' && value instanceof Blob
    || typeof ArrayBuffer !== 'undefined' && value instanceof ArrayBuffer
    || typeof URLSearchParams !== 'undefined' && value instanceof URLSearchParams;
}

async function readResponse(response: Response, responseType?: 'json' | 'blob' | 'text' | 'void') {
  if (response.status === 204 || responseType === 'void') {
    return undefined;
  }
  const contentType = response.headers.get('content-type') ?? '';
  if (responseType === 'blob' || contentType.includes('application/octet-stream')) {
    return response.blob();
  }
  if (responseType === 'text' || contentType.startsWith('text/')) {
    return response.text();
  }
  return response.json();
}

async function readErrorBody(response: Response) {
  const contentType = response.headers.get('content-type') ?? '';
  if (contentType.includes('application/json')) {
    return response.json();
  }
  return response.text();
}
`;
}

function componentType(name, schema) {
  const typeName = typeNameFrom(name);
  if (isObjectSchema(schema) && !schema.allOf && !schema.oneOf && !schema.anyOf) {
    return `export interface ${typeName} ${objectType(schema, typeName)}`;
  }
  return `export type ${typeName} = ${schemaType(schema, typeName)};`;
}

function pathTypes(paths) {
  const pathEntries = [];
  const operationEntries = [];
  const seenOperations = new Map();
  for (const [path, pathItem] of Object.entries(paths)) {
    const methodEntries = [];
    for (const method of HTTP_METHODS) {
      const operation = pathItem?.[method];
      if (!operation) {
        continue;
      }
      const pathParams = parametersType([...(pathItem.parameters || []), ...(operation.parameters || [])], 'path');
      const queryParams = parametersType([...(pathItem.parameters || []), ...(operation.parameters || [])], 'query');
      const body = requestBodyType(operation.requestBody);
      const response = responseType(operation.responses || {});
      methodEntries.push(`${method}: {
  path: ${pathParams};
  query: ${queryParams};
  body: ${body};
  response: ${response};
};`);
      const operationId = uniqueOperationId(operation.operationId || `${method}_${path}`, seenOperations);
      operationEntries.push(`${JSON.stringify(operationId)}: { method: ${JSON.stringify(method)}, path: ${JSON.stringify(path)} },`);
    }
    if (methodEntries.length > 0) {
      pathEntries.push(`${JSON.stringify(path)}: {
${indent(methodEntries.join('\n'), 2)}
};`);
    }
  }
  return {
    apiPaths: pathEntries.join('\n'),
    operationMap: operationEntries.join('\n')
  };
}

function parametersType(parameters, location) {
  const relevant = parameters.filter((parameter) => parameter.in === location);
  if (relevant.length === 0) {
    return 'undefined';
  }
  const required = new Set(relevant.filter((parameter) => parameter.required || location === 'path').map((parameter) => parameter.name));
  const lines = relevant.map((parameter) => {
    const optional = required.has(parameter.name) ? '' : '?';
    return `${propertyName(parameter.name)}${optional}: ${schemaType(parameter.schema || {}, `${location}_${parameter.name}`)};`;
  });
  return `{\n${indent(lines.join('\n'), 2)}\n}`;
}

function requestBodyType(requestBody) {
  if (!requestBody?.content) {
    return 'undefined';
  }
  const content = requestBody.content;
  if (content['application/json']) {
    return schemaType(content['application/json'].schema || {}, 'RequestBody');
  }
  if (content['multipart/form-data']) {
    return 'FormData';
  }
  if (content['application/octet-stream']) {
    return 'Blob | File | ArrayBuffer';
  }
  return 'unknown';
}

function responseType(responses) {
  const response = responses['200'] || responses['201'] || responses['202'] || responses['204'] || responses.default || Object.values(responses)[0];
  if (!response || !response.content || response === responses['204']) {
    return 'void';
  }
  const content = response.content;
  if (content['application/json']) {
    return schemaType(content['application/json'].schema || {}, 'Response');
  }
  if (content['application/octet-stream']) {
    return 'Blob';
  }
  if (content['text/plain']) {
    return 'string';
  }
  return 'unknown';
}

function schemaType(schema, contextName) {
  if (!schema || Object.keys(schema).length === 0) {
    return 'unknown';
  }
  if (schema.$ref) {
    return refType(schema.$ref);
  }
  const nullable = schema.nullable === true || (Array.isArray(schema.type) && schema.type.includes('null'));
  const nonNullType = Array.isArray(schema.type) ? schema.type.find((type) => type !== 'null') : schema.type;
  let type;
  if (schema.oneOf || schema.anyOf) {
    type = (schema.oneOf || schema.anyOf).map((child, index) => schemaType(child, `${contextName}${index}`)).join(' | ');
  } else if (schema.allOf) {
    type = schema.allOf.map((child, index) => schemaType(child, `${contextName}${index}`)).join(' & ');
  } else if (schema.enum) {
    type = schema.enum.map((value) => JSON.stringify(value)).join(' | ') || 'string';
  } else if (nonNullType === 'array') {
    type = `${schemaType(schema.items || {}, `${contextName}Item`)}[]`;
  } else if (nonNullType === 'object' || schema.properties || schema.additionalProperties) {
    type = objectType(schema, contextName);
  } else if (nonNullType === 'integer' || nonNullType === 'number') {
    type = 'number';
  } else if (nonNullType === 'boolean') {
    type = 'boolean';
  } else if (nonNullType === 'string') {
    type = schema.format === 'binary' ? 'Blob' : 'string';
  } else {
    type = 'unknown';
  }
  return nullable ? `${type} | null` : type;
}

function objectType(schema, contextName) {
  const properties = schema.properties || {};
  const required = new Set(schema.required || []);
  const lines = Object.entries(properties).map(([name, childSchema]) => {
    const optional = required.has(name) ? '' : '?';
    return `${propertyName(name)}${optional}: ${schemaType(childSchema, `${contextName}_${name}`)};`;
  });
  if (schema.additionalProperties && schema.additionalProperties !== false) {
    const additionalType = schema.additionalProperties === true ? 'unknown' : schemaType(schema.additionalProperties, `${contextName}Additional`);
    lines.push(`[key: string]: ${additionalType};`);
  }
  return lines.length === 0 ? 'Record<string, unknown>' : `{\n${indent(lines.join('\n'), 2)}\n}`;
}

function isObjectSchema(schema) {
  return schema?.type === 'object' || schema?.properties || schema?.additionalProperties;
}

function refType(ref) {
  return typeNameFrom(decodeURIComponent(ref.split('/').at(-1)));
}

function typeNameFrom(name) {
  const cleaned = String(name)
    .replace(/[^A-Za-z0-9_]/g, '_')
    .replace(/^[^A-Za-z_]+/, '');
  return cleaned || 'GeneratedType';
}

function propertyName(name) {
  return /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(name) ? name : JSON.stringify(name);
}

function uniqueOperationId(operationId, seen) {
  const base = operationId.replace(/[^A-Za-z0-9_$]/g, '_').replace(/^[^A-Za-z_$]+/, '') || 'operation';
  const count = seen.get(base) || 0;
  seen.set(base, count + 1);
  return count === 0 ? base : `${base}_${count + 1}`;
}

function indent(value, spaces) {
  const padding = ' '.repeat(spaces);
  return value.split('\n').map((line) => (line ? `${padding}${line}` : line)).join('\n');
}

function relative(path) {
  return path.replace(`${root}/`, '');
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
